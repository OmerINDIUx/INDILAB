(() => {
  try {
    console.log("‚úÖ Script menu.js inicializado...");

    // Verificar GSAP
    if (typeof gsap === "undefined") {
      console.error("‚ùå Error: GSAP no est√° cargado. Revisa la importaci√≥n del script.");
      return; // ‚úÖ ahora s√≠ se puede usar porque estamos dentro de una funci√≥n
    }
    console.log("‚úÖ GSAP detectado correctamente.");

    // Obtener elementos del DOM
    const menuToggle = document.getElementById('menu-toggle');
    const fullscreenMenu = document.getElementById('fullscreen-menu');
    const closeButton = document.getElementById('close-menu');
    const menuLinks = fullscreenMenu?.querySelectorAll('.menu__item') || [];

    if (!fullscreenMenu) {
      console.error("‚ùå Error: No se encontr√≥ el elemento #fullscreen-menu en el DOM.");
      return;
    }
    console.log("‚úÖ #fullscreen-menu encontrado en el DOM.");

    let menuOpen = false;

    function openMenu() {
      try {
        if (menuOpen) return;
        menuOpen = true;
        fullscreenMenu.classList.add('show');

        gsap.set(fullscreenMenu, {
          transformOrigin: "top left",
          transformPerspective: 1200
        });

        gsap.fromTo(
          fullscreenMenu,
          { rotateZ: 90, rotateY: -90, opacity: 1 },
          { rotateZ: 0, rotateY: 0, opacity: 1, duration: 1.5, ease: "power4.out" }
        );

        console.log("‚úÖ Men√∫ abierto correctamente.");
      } catch (err) {
        console.error("‚ùå Error al abrir el men√∫:", err);
      }
    }

    function closeMenu() {
      try {
        if (!menuOpen) return;
        menuOpen = false;
        gsap.to(fullscreenMenu, {
          rotateZ: 90,
          rotateY: -90,
          opacity: 0,
          transformOrigin: "top left",
          transformPerspective: 1200,
          duration: 0.6,
          ease: "power4.in",
          onComplete: () => {
            fullscreenMenu.classList.remove('show');
            console.log("‚úÖ Men√∫ cerrado correctamente.");
          }
        });
      } catch (err) {
        console.error("‚ùå Error al cerrar el men√∫:", err);
      }
    }

    // Listeners
    if (menuToggle) {
      menuToggle.addEventListener('click', () => {
        if (!menuOpen) openMenu();
      });
      console.log("‚úÖ Listener agregado a #menu-toggle.");
    } else {
      console.warn("‚ö†Ô∏è Advertencia: No se encontr√≥ #menu-toggle en el DOM.");
    }

    if (closeButton) {
      closeButton.addEventListener('click', closeMenu);
      console.log("‚úÖ Listener agregado a #close-menu.");
    } else {
      console.warn("‚ö†Ô∏è Advertencia: No se encontr√≥ #close-menu en el DOM.");
    }

    document.addEventListener('keydown', (e) => {
      if (e.key === 'Escape' && menuOpen) closeMenu();
    });
    console.log("‚úÖ Listener agregado para tecla ESC.");

    if (menuLinks.length > 0) {
      menuLinks.forEach(link => {
        link.addEventListener('click', (e) => {
          try {
            e.preventDefault();
            closeMenu();
            const href = link.getAttribute('href');

            if (href && href.startsWith('#')) {
              setTimeout(() => {
                const target = document.querySelector(href);
                if (target) {
                  target.scrollIntoView({ behavior: 'smooth' });
                  console.log(`‚úÖ Scroll hacia ${href} realizado correctamente.`);
                } else {
                  console.warn(`‚ö†Ô∏è Advertencia: No se encontr√≥ el destino ${href} en el DOM.`);
                }
              }, 700);
            } else if (href) {
              console.log(`‚û°Ô∏è Redirigiendo a: ${href}`);
              window.location.href = href;
            } else {
              console.warn("‚ö†Ô∏è Advertencia: Link sin 'href' detectado.");
            }
          } catch (err) {
            console.error("‚ùå Error al manejar el click de un enlace del men√∫:", err);
          }
        });
      });
      console.log(`‚úÖ ${menuLinks.length} enlaces del men√∫ listos.`);
    } else {
      console.warn("‚ö†Ô∏è Advertencia: No se encontraron enlaces con clase .menu__item.");
    }

    console.log("üéâ Men√∫ inicializado correctamente.");

  } catch (error) {
    console.error("‚ùå Error cr√≠tico en la inicializaci√≥n del men√∫:", error);
  }
})();
